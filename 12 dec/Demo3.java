// Program showing the concept of wrapper class

class Demo3{
    public static void main(String args[]){
        System.out.println(""+Character.isDigit('5'));
        System.out.println(""+Character.isAlphabetic('5'));
        System.out.println(""+Character.isSpace('5'));
        System.out.println(""+Character.isWhitespace('4'));
    }
}